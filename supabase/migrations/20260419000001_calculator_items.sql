-- =============================================================================
-- 비용 계산기 데이터 테이블
--   국가별 준비 항목과 비용을 저장. 공개 읽기(anon select), 인증 사용자만 수정.
--   country_order 는 numeric 으로 두어 "호주(고양이)" 처럼 부모 직후에 삽입
--   되는 변종을 3.5 같은 소수로 정렬.
-- =============================================================================

create table calculator_items (
  id            bigint generated always as identity primary key,
  country       text not null,
  item_name     text not null,
  cost          integer not null check (cost >= 0),
  item_order    integer not null default 0,
  country_order numeric not null default 0,
  created_at    timestamptz not null default now(),
  updated_at    timestamptz not null default now(),
  unique (country, item_name)
);

create index calculator_items_country_idx on calculator_items(country);
create index calculator_items_order_idx   on calculator_items(country_order, item_order);

-- 공개 읽기 / 인증된 사용자만 쓰기
alter table calculator_items enable row level security;

create policy "Anyone can read calculator_items"
  on calculator_items for select
  using (true);

create policy "Authenticated can insert calculator_items"
  on calculator_items for insert
  to authenticated
  with check (true);

create policy "Authenticated can update calculator_items"
  on calculator_items for update
  to authenticated
  using (true)
  with check (true);

create policy "Authenticated can delete calculator_items"
  on calculator_items for delete
  to authenticated
  using (true);

